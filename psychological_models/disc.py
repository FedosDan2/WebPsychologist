def calculate_disc_score(answers):
    """
    answers - список из 5 букв (например: ['D', 'I', 'S', 'C', 'D'])
    Возвращает словарь с результатами
    """
    scores = {"D": 0, "I": 0, "S": 0, "C": 0}
    
    for answer in answers:
        if answer in scores:
            scores[answer] += 1
    
    return scores

def get_disc_profile(scores):
    """Определяет доминирующий тип DISC"""
    max_score = max(scores.values())
    dominant_types = [t for t, s in scores.items() if s == max_score]
    
    return dominant_types[0] if dominant_types else "S"  # по умолчанию Steadiness